//To run the program which begins with initial value of 10 unread emails.
//Further to get the output as 10 and use the increment operator ++ [or count + 1], and get the new output of 11 unread emails.
fun main() {
    var count = 10
    println("You have $count unread emails.")
    count++
    println("You have $count unread emails.")
}
